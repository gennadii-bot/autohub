async def seed() -> None:
    """Idempotent seed: creates regions and cities if not exist."""
    async with async_session_maker() as session:
        region_by_name: dict[str, Region] = {}

        for name in REGIONS:
            result = await session.execute(select(Region).where(Region.name == name))
            existing = result.scalar_one_or_none()
            if existing:
                region_by_name[name] = existing
            else:
                r = Region(name=name)
                session.add(r)
                await session.flush()
                region_by_name[name] = r

        created = 0
        for city_name, region_name, lat, lng in CITIES:
            region = region_by_name.get(region_name)
            if not region:
                logger.warning("Region not found: %s", region_name)
                continue
            result = await session.execute(
                select(City).where(
                    City.name == city_name,
                    City.region_id == region.id,
                )
            )
            if result.scalar_one_or_none():
                continue
            city = City(
                name=city_name,
                region_id=region.id,
                lat=lat,
                lng=lng,
            )
            session.add(city)
            created += 1

        await session.commit()
        logger.info("Seeded %d regions, %d cities (created %d new)", len(REGIONS), len(CITIES), created)


if __name__ == "__main__":
    asyncio.run(seed())

